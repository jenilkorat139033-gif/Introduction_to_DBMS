CREATE TABLE JOB(
JOB_ID VARCHAR2(10),
JOB_NAME VARCHAR2(30),
MIN_SAL NUMBER(8),
MAX_SAL NUMBER(8)
);

CREATE TABLE EMPLOYEE(
EMP_NO NUMBER(5),
EMP_NAME VARCHAR2(20),
EMP_SAL NUMBER(10),
EMP_COMM NUMBER(10),
DEPT_NO NUMBER(5)
);

CREATE TABLE DEPOSIT(
A_NO NUMBER(5),
CNAME VARCHAR2(20),
BNAME VARCHAR2(20),
AMOUNT NUMBER(10),
DT DATE
);
